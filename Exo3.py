nombre1 = int(input("Entrez un nombre : "))
nombre2 = int(input("Entrez un 2ème nombre : "))
nombre3 = int(input("Entrez un 3ème nombre : "))

plus_grand = max(nombre1,nombre2,nombre3)
plus_petit = min(nombre1,nombre2,nombre3)

print("Le plus grand nombre est : ", plus_grand)
print("Le plus petit nombre est : ", plus_petit)

    


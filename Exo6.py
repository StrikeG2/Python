nombre1 = int(input("Entrez un nombre"))
nombre2 = int(input("Entrez un 2eme nombre"))
nombre3 = int(input("Entrez un 3eme nombre"))
nombre4 = int(input("Entrez un 4eme nombre"))
nombre5 = int(input("Entrez un 5eme nombre"))

plus_grand = max(nombre1,nombre2,nombre3,nombre4,nombre5)
plus_petit = min(nombre1,nombre2,nombre3,nombre4,nombre5)

print("Le plus grand nombre est : ",plus_grand)
print("Le plus petit nombre est : ",plus_petit)
